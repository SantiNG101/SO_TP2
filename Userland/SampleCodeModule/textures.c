void drawTexture(TextureType type, int x, int y, uint64_t colour){
    //const Texture * texture = Textures[type];

    // Draws texture on screen.
    int screenX = x * TEXTURE_WIDTH;
    int screenY = y * TEXTURE_HEIGHT;
    int pixIsPresent;

    for(int i = 0; i < TEXTURE_WIDTH; i++)
        for(int j = 0; j < TEXTURE_HEIGHT; j++){
            pixIsPresent = (Textures[type])[j] & (1 << (TEXTURE_WIDTH - i - 1));
            putPixel(screenX + i, screenY + j, pixIsPresent ? colour : BLACK);
        }
}